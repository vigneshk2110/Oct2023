#include<stdio.h>


int main(){
   
    int arr[] = {10,20,30,40,50};

    int *ptr = arr;
    printf("%d\n", *++ptr + 3);
    printf("%d\n", *(ptr-- + 2)+5);
    printf("%d\n", *(ptr+3)-10);

    return 0;
}

